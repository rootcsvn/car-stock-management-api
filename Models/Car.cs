public class Car
{
    // Property to hold the make (brand) of the car, e.g., "Audi"
    public string Make { get; set; }

    // Property to hold the model of the car, e.g., "A4"
    public string Model { get; set; }

    // Property to hold the year of the car, e.g., 2018
    public int Year { get; set; }

    // Property to track the number of cars in stock for this make and model
    public int Stock { get; set; }
}